# ── STEP 2: Base Indicators ────────────────────────────────────────
def add_indicators(df):
    df['SMA_20']      = df['Close'].rolling(20).mean()
    df['SMA_50']      = df['Close'].rolling(50).mean()
    df['EMA_20']      = df['Close'].ewm(span=20, adjust=False).mean()
    delta             = df['Close'].diff()
    gain              = delta.clip(lower=0)
    loss              = -delta.clip(upper=0)
    df['RSI']         = 100 - (100 / (1 + gain.rolling(14).mean() / loss.rolling(14).mean()))
    ema12             = df['Close'].ewm(span=12, adjust=False).mean()
    ema26             = df['Close'].ewm(span=26, adjust=False).mean()
    df['MACD']        = ema12 - ema26
    df['MACD_Signal'] = df['MACD'].ewm(span=9, adjust=False).mean()
    df['MACD_Hist']   = df['MACD'] - df['MACD_Signal']
    rm                = df['Close'].rolling(20).mean()
    rs                = df['Close'].rolling(20).std()
    df['BB_Upper']    = rm + 2 * rs
    df['BB_Lower']    = rm - 2 * rs
    df['BB_Width']    = df['BB_Upper'] - df['BB_Lower']
    df['Mom_5']       = df['Close'].pct_change(5)
    df['Mom_10']      = df['Close'].pct_change(10)
    df['Mom_30']      = df['Close'].pct_change(30)
    df['Vol_MA_10']   = df['Volume'].rolling(10).mean()
    df['Vol_Ratio']   = df['Volume'] / df['Vol_MA_10']
    df.dropna(inplace=True)
    print(f"✅ Base indicators added")
    return df

# ── STEP 3: Advanced Indicators ────────────────────────────────────
def add_advanced_indicators(df):
    df['Price_Range']    = df['High'] - df['Low']
    df['Gap']            = df['Open'] - df['Close'].shift(1)
    df['Body']           = abs(df['Close'] - df['Open'])
    df['Volatility_10']  = df['Close'].pct_change().rolling(10).std() * 100
    df['Volatility_20']  = df['Close'].pct_change().rolling(20).std() * 100
    df['SMA_Cross']      = (df['SMA_20'] - df['SMA_50']) / df['SMA_50'] * 100
    df['Price_vs_SMA20'] = (df['Close']  - df['SMA_20']) / df['SMA_20'] * 100
    df['Price_vs_SMA50'] = (df['Close']  - df['SMA_50']) / df['SMA_50'] * 100
    df['RSI_Overbought'] = (df['RSI'] > 70).astype(int)
    df['RSI_Oversold']   = (df['RSI'] < 30).astype(int)
    df['RSI_Change']     = df['RSI'].diff()
    df['MACD_Cross']     = (df['MACD'] > df['MACD_Signal']).astype(int)
    df['MACD_Change']    = df['MACD_Hist'].diff()
    df['Vol_Spike']      = (df['Vol_Ratio'] > 2).astype(int)
    df['Vol_Change']     = df['Volume'].pct_change()
    for lag in [1, 2, 3, 5]:
        df[f'Close_Lag_{lag}']  = df['Close'].shift(lag)
        df[f'Return_Lag_{lag}'] = df['Close'].pct_change(lag)
    df['DayOfWeek']      = df.index.dayofweek
    df.dropna(inplace=True)
    print(f"✅ Advanced indicators added")
    return df

