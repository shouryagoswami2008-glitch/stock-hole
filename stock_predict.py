def get_daily_sentiment(news_df):
    label_map        = {'positive': 1, 'neutral': 0, 'negative': -1}
    news_df['score'] = news_df['label'].map(label_map) * news_df['confidence']
    daily            = (news_df.groupby('date')['score'].mean()
                        .reset_index().rename(columns={'score':'sentiment_score'}))
    daily['date']    = pd.to_datetime(daily['date'])
    print(f"✅ Daily sentiment: {len(daily)} days")
    return daily

def merge_sentiment_with_stock(df, daily_sentiment):
    df            = df.copy()
    df['date_dt'] = pd.to_datetime(df.index.strftime('%Y-%m-%d'))
    merged        = pd.merge_asof(
        df.sort_values('date_dt'),
        daily_sentiment.rename(columns={'date':'date_dt'}),
        on='date_dt', direction='backward'
    )
    merged['sentiment_score'] = merged['sentiment_score'].fillna(0)
    merged.index              = df.index
    merged.drop(columns=['date_dt'], inplace=True)
    print(f"✅ Sentiment merged! Shape: {merged.shape}")
    return merged

