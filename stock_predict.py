def fetch_news_finnhub(ticker_symbol, days_back=30):
    client     = finnhub.Client(api_key=FINNHUB_KEY)
    end_date   = datetime.today().strftime('%Y-%m-%d')
    start_date = (datetime.today() - timedelta(days=days_back)).strftime('%Y-%m-%d')

    # Try direct ticker first
    news = client.company_news(ticker_symbol, _from=start_date, to=end_date)

    # If no results (common for Indian stocks), search by company name
    if len(news) == 0:
        print(f"⚠️  No Finnhub news for {ticker_symbol}, switching to keyword search...")

        # Get company name — from map or from yfinance
        if ticker_symbol in INDIAN_COMPANIES:
            company_name = INDIAN_COMPANIES[ticker_symbol]
        else:
            try:
                info         = yf.Ticker(ticker_symbol).info
                company_name = info.get('longName', ticker_symbol.replace('.NS','').replace('.BO',''))
            except:
                company_name = ticker_symbol.replace('.NS','').replace('.BO','')

        print(f"   Searching news for: {company_name}")

        # Use Finnhub general news search
        news = client.general_news('general', min_id=0)
        news = [n for n in news if
                company_name.lower().split()[0] in n.get('headline','').lower() or
                company_name.lower().split()[0] in n.get('summary','').lower()]

    headlines = []
    for article in news:
        try:
            headlines.append({
                'date'    : datetime.fromtimestamp(article['datetime']).strftime('%Y-%m-%d'),
                'headline': article['headline'],
                'source'  : article['source'],
                'summary' : article.get('summary', '')
            })
        except:
            continue

    news_df = pd.DataFrame(headlines).drop_duplicates(subset='headline') \
              if headlines else pd.DataFrame(columns=['date','headline','source','summary'])

    if len(news_df) == 0:
        print(f"⚠️  No news found — using neutral sentiment")
        news_df = pd.DataFrame([{
            'date'    : datetime.today().strftime('%Y-%m-%d'),
            'headline': f'{ticker_symbol} stock analysis',
            'source'  : 'fallback',
            'summary' : ''
        }])

    print(f"✅ Fetched {len(news_df)} articles for {ticker_symbol}")
    return news_df

